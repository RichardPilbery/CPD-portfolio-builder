            <p class="text-lg pb-4">The development review process consists of four stages:
                <ol class="list-decimal pl-4">
                    <li>Reviewing how individuals are applying their knowledge and skills to meet the demands of their current post and identifying whether they have any development needs - the demands of the post are described in a NHS KSF outline for the post</li>
                    <li>Developing a Personal Development Plan for that individual detailing the learning and development to take place in the coming months and the date of the next review</li>
                    <li>Learning and development for the individual supported by their reviewer</li>
                    <li>Evaluating the learning and development and reflecting on how it has been applies to work</li>
                </ol>
            </p>

